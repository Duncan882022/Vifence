const MEDIUM_SEVERITY_TYPES: ViolationType[] = ['no-helmet']

export function getViolationSeverity(type: ViolationType): ViolationSeverity {
  if (HIGH_SEVERITY_TYPES.includes(type)) return 'high'
  if (MEDIUM_SEVERITY_TYPES.includes(type)) return 'medium'
  return 'low'
}

